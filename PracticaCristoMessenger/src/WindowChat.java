    public void sendMessage(){
        Message msg = new Message();
        msg.setTransmitter(this.myUserController.getUser().getId());
        msg.setReceiver(this.FriendList.getSelectedValue());
        msg.setText(this.MessageTextField.getText());
        this.myUserController.sendMessage(msg);
        this.MessageTextField.setText("");
        this.initChat(this.FriendList.getSelectedValue());
    }
