    public void initChat(String idFriend){
        this.MessageTextArea.setText("");
        this.MessageTextField.setText("");
        this.ChatFriendHeader.setText("");
        String FullNameFriend = "";
        for (int i = 0; i < this.myFriendList.size(); i++) {
            if (this.myFriendList.get(i).getId().equals(idFriend)) {
                FullNameFriend += this.myFriendList.get(i).getName() + " ";
                FullNameFriend += this.myFriendList.get(i).getFirstSurname() + " ";
                FullNameFriend += this.myFriendList.get(i).getSecondSurname();
                myFriendMesages = this.myUserController.loadMessages(this.myFriendList.get(i).getId());
                this.PrintFriendMessages();
                i = this.myFriendList.size();
            }
        }
        this.ChatFriendHeader.setText(FullNameFriend);
        rsscalelabel.RSScaleLabel.setScaleLabel(ChatFriendImage, "src\\Images\\logo.jpg");
    }
    public void PrintFriendMessages(){
        for (int i = 0; i < this.myFriendMesages.size(); i++) {
            this.MessageTextArea.setText(this.MessageTextArea.getText() + this.myFriendMesages.get(i).getTransmitter() +
                    ": " + this.myFriendMesages.get(i).getText() + "\n");
        }
    }
    public void PrintFriendList(){
        DefaultListModel<String> model = new DefaultListModel<>();
        FriendList.setCellRenderer(new ListaRender());
        for (int i = 0; i < myFriendList.size(); i++) {
            model.addElement(myFriendList.get(i).getId());
        }
        FriendList.setModel(model);
    }
